public class Operasi {
    public static void main(String[] args){
        //pengistiharaan
        int no1,no2;
        int a,b,c,d,e;
        //umpukkan
        no1=85;
        no2=2;
        //operasi
        a=no1+no2;
        b=no1-no2;
        c=no1*no2;
        d=no1/no2;
        e=no1%no2;
        //paparan output
        System.out.println("Hasil tambah:"+a);
        System.out.println("Hasil tolak:"+b);
        System.out.println("Hasil darab:"+c);
        System.out.println("Hasil bahagi:"+d);
        System.out.println("Hasil bahagi:"+e);
    }
    
}
